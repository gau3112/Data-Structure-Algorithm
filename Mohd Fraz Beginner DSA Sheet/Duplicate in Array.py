def findDuplicate(arr):
    
    # using XOR to find Duplicates (It could only be used if numbers are in a range)
    
    min = 1
    max = len(arr)
    xor_range  = 0
    xor_array  = 0
    for i in range(min,max,1):
        xor_range = xor_range^i
    for i in arr:
        xor_array = xor_array^i
    return xor_array^xor_range
